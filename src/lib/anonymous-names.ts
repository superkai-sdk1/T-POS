/**
 * Returns a deterministic anonymous client name based on a seed string (typically check ID).
 */
export function getAnonymousClientName(seed: string): string {
  if (!seed) return ANON_CLIENT_NAMES[0];
  let hash = 0;
  for (let i = 0; i < seed.length; i++) {
    hash = (hash * 31 + seed.charCodeAt(i)) >>> 0;
  }
  const idx = hash % ANON_CLIENT_NAMES.length;
  return ANON_CLIENT_NAMES[idx];
}
